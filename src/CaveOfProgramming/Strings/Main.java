package CaveOfProgramming.Strings;

public class Main {
    public static void main(String[] args) {
        int myInt = 7;
        String text = "Hello agent: ";

        System.out.println(text + " " + myInt);

        String hello = "Hello";
        String blank = " ";
        String name = "Papa Emeritus IV";
        String Greeting = hello + blank + name;

        System.out.println(Greeting);

        double myDouble = 7.8;
        System.out.println("My number is: " + myDouble + ".");
    }
}
